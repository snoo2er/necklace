package by.hryshchanka.task2.action;

public class CreatorException extends Exception{

	private static final long serialVersionUID = 1L;

	public CreatorException() {
		super();
	}

	public CreatorException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public CreatorException(String arg0) {
		super(arg0);
	}

	public CreatorException(Throwable arg0) {
		super(arg0);
	}

}
